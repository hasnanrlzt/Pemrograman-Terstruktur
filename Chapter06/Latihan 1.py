def Pythagoras(a, b, c):
    if (c ** 2 == (a ** 2) + (b ** 2)):
        print('True!!! Sisi-sisi segitiga',a,b,c,' merupakan triple Pythagoras')
    else:
        print('False!!! Sisi-sisi segitiga ',a,b,c,'bukan merupakan triple Pythagoras')

Pythagoras(3, 4, 5)
Pythagoras(5, 9, 12)
Pythagoras(8, 6, 10)
Pythagoras(7, 8, 11)

